#include "StringSplit.h"

// boost conflicts with enable_if, so we offer our split function here replacing boost::split

std::vector<std::string> &__inner_split(const std::string &s, char delim, std::vector<std::string> &elems) {
    std::stringstream ss(s);
    std::string item;
    while(std::getline(ss, item, delim)) {
        elems.push_back(item);
    }
    return elems;
}

std::vector<std::string> __inner_split(const std::string &s, char delim) {
    std::vector<std::string> elems;
    return __inner_split(s, delim, elems);
}
